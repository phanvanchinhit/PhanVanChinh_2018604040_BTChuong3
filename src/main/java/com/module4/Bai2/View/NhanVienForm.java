    void showData() {
        Vector column = new Vector();
        column.add("Mã");
        column.add("Tên");
        column.add("Ngày sinh");
        column.add("Số điện thoại");
        column.add("Địa chỉ");
        /* Set Column Header lên DefaultTableModel */
        model.setColumnIdentifiers(column);
        List<NhanVien> list = nhanVienController.getNhanVien();
        for (int i = 0; i < list.size(); i++) {
            NhanVien nhanVien = (NhanVien) list.get(i);
            Vector row = new Vector();
            row.add(nhanVien.getManv());
            row.add(nhanVien.getTennv());
            row.add(nhanVien.getBirthday());
            row.add(nhanVien.getSdt());
            row.add(nhanVien.getDiachi());
            model.addRow(row);
            jTable1.setModel(model);
        }
    }

